function imagetitleshow() {
    const imagetitle = document.getElementById("imagedesc");

    imagetitle.style.visibility = 'visible';
    imagetitle.style.opacity = '1';
}
function imagetitlehide() {
    const imagetitle = document.getElementById("imagedesc");

    imagetitle.style.visibility = 'hidden';
    imagetitle.style.opacity = '0';
}